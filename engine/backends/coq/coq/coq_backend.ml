let translate _ _ ~bundles:_ (items : AST.item list) :
  Types.file list =
  let my_printer = new MyPrinter.printer in

  (* let _ = Gen.map (fun apply -> *)
  (*     let printer = new MyPrinter.printer in *)
  (*     let doc = apply printer in *)
  (*     let buf = Buffer.create 0 in *)
  (*     PPrint.ToBuffer.pretty 1.0 80 buf doc; *)
  (*     (Buffer.contents buf (\* , printer#get_span_data () *\))) in *)

  U.group_items_by_namespace items
  |> Map.to_alist
  |> List.map ~f:(fun (ns, items) ->
         let mod_name =
           String.concat ~sep:"_"
             (List.map
                ~f:(map_first_letter String.uppercase)
                (fst ns :: snd ns))
         in

         Types.
           {
             path = mod_name ^ ".v";
             contents =
               hardcoded_coq_headers ^ "\n" ^
               String.concat ~sep:"\n" (
                 List.map
                   ~f:(fun item ->
                       let buf = Buffer.create 0 in
                       PPrint.ToBuffer.pretty 1.0 80 buf (my_printer#entrypoint_item item);
                       Buffer.contents buf
                     )
                   items);
             sourcemap = None;
           })

